package simpleprogramofjava;

public class Operator {
public static void main (String[]args) {
    int x=25;
    int y=45;
    
    System.out.println(x+y);
    System.out.println(x-y);
    System.out.println(x*y);
    System.out.println(x/y);
    
    int a=20;
    System.out.println(a++);
    int b=30;
    System.out.println(b--);
    
 //comparison operator
    
      
    System.out.println(x==y);
    System.out.println(x!=y);
    System.out.println(x>y);
    System.out.println(x<y);
    System.out.println(x<=y);
    System.out.println(x>=y);
    
    
// logical operator
    
    
    
    System.out.println((x==y)&&(x>y));
    System.out.println((x==y)&&(x<y));
    
}
}